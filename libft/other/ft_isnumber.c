#include "libft.h"

int	ft_isnumber(char *str)
{
	char	*p;

	p = str;
	if (*p == '-' || *p == '+')
		p++;
	while (*p)
	{
		if (!ft_isdigit(*p))
			return (0);
		p++;
	}
	return (1);
}
